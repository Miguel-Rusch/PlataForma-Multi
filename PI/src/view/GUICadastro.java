    public void cadastro() throws SQLException{
        try {
            

        
        loginVO LVO = new loginVO();
        
        LVO.setEmail(jtfEmail.getText());
        LVO.setSenha(jtfSenha.getText());
        LVO.setUsuario(jtfUsuario.getText());
        
        loginDAO LDAO = new loginDAO();
        LDAO.cadastrar(LVO);
        
        
        GUILog log = new GUILog();
        log.setVisible(true);
        dispose();
                JOptionPane.showMessageDialog(null,"Cadastro sucedido  ");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null," erro GUIcad " + e.getMessage());
        }
    }

