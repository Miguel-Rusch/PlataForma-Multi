       public static void gerarLabel(JTextArea panel,String mensagem,int num) {
           conversao conv = new conversao();
           mensagem = conv.formatacaoMenagem(mensagem,num);
        JLabel label = new JLabel(mensagem);
        //pro bot?o ficar com tamanho maximo no BoxLayout
        // ? preciso definir a dimens?o maxima do botao
        //Integer.MAX_VALUE definir a maior largura possivel
        label.setMaximumSize(new Dimension(Integer.MAX_VALUE, label.getMinimumSize().height));
        //adicionei um espa?amento pro proximo botao n?o
        //ficar colado, voc? pode alterar a vontade
        panel.add(Box.createRigidArea(new Dimension(0, 5)));
         
        panel.add(label);
       
         

         
        //o revalidate deve ser aplicado ao componente root
        //neste caso, o JFrame
        //pra evitar problemas com tamanho de outros containers
        //da tela
        panel.getRootPane().revalidate();
    }
